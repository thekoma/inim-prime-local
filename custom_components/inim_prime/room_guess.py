def guess_room(label: str, language: str | None = None) -> str | None:
    """Return the canonical room name for a zone label, or None.

    ``language`` selects the vocabulary (English is always merged in too).
    """
    folded = _fold(label)
    lang = normalize_language(language)
    # normalize_language only returns a supported base code or "en"; both are
    # always present in _ROOMS. The language vocabulary is tried before English
    # so it wins ties on equal-length matches.
    vocabs = (_ROOMS["en"],) if lang == "en" else (_ROOMS[lang], _ROOMS["en"])
    best_room: str | None = None
    best_len = 0
    for vocab in vocabs:
        for room, tokens in vocab.items():
            for token in tokens:
                if len(token) > best_len and token in folded:
                    best_len = len(token)
                    best_room = room
    return best_room
